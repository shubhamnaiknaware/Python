l = [1, 2, 3, 4, 5, 6]
# add element to list
l.append(7)
print(l)
# pop last element of list
l.pop()
print(l)
# pop or remove element at given index
l.pop(2)
print(l)
# remove given first occurance of value from list
l.remove(5)
print(l)
# delete using index
del l[3]
print(l)
# delete elements in range
del l[1:2]
print(l)
# clear all elements of list
l.clear()
print(l)
