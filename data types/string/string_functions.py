# string methods
course = " python programming"
# string method upper-returns upper case values
print(course.upper())
# string method lower -returns lower case values
print(course.lower())
# string method lower capitalizes all first letters in words
print(course.title())

# string method find finds sub-string in string
print(course.find("pro"))

# string method to strip elements from string
print(course.strip())

# string method replace replaces particular string in program
print(course.replace("p", "j"))

# string method in -returns boolean values
print("pro" in course)
print("swift" not in course)
