course = "python programming"
print(len(course))
# print nth element string word
print(course[0])
# print last element
print(course[-1])
# print elements from nth index to end of file
print(course[5:])
# print elements from start to nth index
print(course[:10])
# print elements with step 2
print(course[::2])
# print elements with nth to mth index with step 3
print(course[1:15:2])
# print reverse string
print(course[::-1])
